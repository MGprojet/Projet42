char *ft_strtrim(char const *s1, char const *set)
{
    int i;
    int j;

    i = 0;
    j = 0; 
    if (s1 == NULL || set == NULL)
        return (NULL);
    while (s1[i])
    {
        if (s1[i] == set)
        {
            j++;
        }
    }
}
